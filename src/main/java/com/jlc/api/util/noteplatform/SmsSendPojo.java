package com.jlc.api.util.noteplatform;

public class SmsSendPojo {
	
	private String url;
	
	private String account;
	
	private String pswd;
	
	private Boolean needstatus;
	
	private String product;
	
	private String extno;
	
	private String mobile_r;

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}

	public String getPswd() {
		return pswd;
	}

	public void setPswd(String pswd) {
		this.pswd = pswd;
	}

	public Boolean getNeedstatus() {
		return needstatus;
	}

	public void setNeedstatus(Boolean needstatus) {
		this.needstatus = needstatus;
	}

	public String getProduct() {
		return product;
	}

	public void setProduct(String product) {
		this.product = product;
	}

	public String getExtno() {
		return extno;
	}

	public void setExtno(String extno) {
		this.extno = extno;
	}

	public String getMobile_r() {
		return mobile_r;
	}

	public void setMobile_r(String mobileR) {
		mobile_r = mobileR;
	}
	

}
